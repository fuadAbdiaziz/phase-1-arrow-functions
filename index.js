// Define the divide function expression
const divide = function() {
    return 2000 / 100;
  }
  
  // Define the square arrow function
  const square = (num) => {
    return num * num;
  }
  
  // Define the add arrow function
  const add = (num1, num2) => {
    return num1 + num2;
  }
  
  // Test the functions
  console.log(divide()); // Output: 20
  console.log(square(5)); // Output: 25
  console.log(add(2, 3)); // Output: 5
  